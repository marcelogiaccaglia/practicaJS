/* ---------- // OBJETO MATH // ------------- */

/* Es un Objeto estatico con propiedades y metodos */

console.log(Math.PI);

console.log(Math.abs(-7.8)); //Devuelve un valor absoluto o sea nunca negativo

console.log(Math.ceil(7.7)); //Devuelve el numero entero mas alto

console.log(Math.floor(7.2)); //Devuelve el nuemero entero mas bajo

console.log(Math.round(7.5)); //Devuelve el valor entero mas cercano

console.log(Math.sqrt(81)); //Realiza la raiz cuadrada del valor

console.log(Math.pow(2, 5)); //Calcula la potencia (1° valor base, 2° valor exponente)

console.log(Math.sign(-7.8)); //Indica el signo del numero (-1, 0, 1)

console.log(Math.random()); //Arroja un numero aleatoreo del 0 al 1 (default)
console.log(Math.random() * 1000); //Se le modifica el dafault
